func (h *Handler) CheckUsername(c *gin.Context) {
	username := c.Query("username")
	if username == "" {
		c.JSON(http.StatusBadRequest, gin.H{"error": "username is required"})
		return
	}
	available, err := h.service.CheckUsernameAvailable(c.Request.Context(), username)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, gin.H{"available": available, "username": username})
}

func (h *Handler) GenerateUsername(c *gin.Context) {
	fullName := c.Query("name")
	if fullName == "" {
		fullName = "user"
	}
	username, err := h.service.GenerateUsername(c.Request.Context(), fullName)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, gin.H{"username": username})
}

func (h *Handler) Logout(c *gin.Context) {
	userID, _ := c.Get("user_id")
	if err := h.service.Logout(c.Request.Context(), userID.(string)); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, gin.H{"message": "Logged out successfully"})
}
